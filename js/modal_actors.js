  document.addEventListener('DOMContentLoaded', (event) => {
    const dramas = document.querySelectorAll('.drama');
    const modal = document.getElementById("drama-modal");
    const closeModal = document.getElementsByClassName("close")[0];
    const modalName = document.getElementById("modal-name");
    const modalBirthdate = document.getElementById("modal-birthdate");
    const modalBiography = document.getElementById("modal-biography");
    const modalOtherWorks = document.getElementById("modal-other-works");
    const modalImages = document.getElementById("modal-images");

    function updateModalContent(index) {
        // Obtener la preferencia de idioma del localStorage
        let preferredLanguage = localStorage.getItem('preferredLanguage');
        
        // Definir el arreglo de dramas según la preferencia de idioma
        let dramasArray = [];
        if (preferredLanguage === 'en') {
            dramasArray = kDramasEn;
        } else {
            dramasArray = kDramasEs; // Por defecto, mostrar en español si no está configurado
        }

        const language = localStorage.getItem('preferredLanguage') || 'en'; // Obtener el idioma preferido, por defecto inglés ('en')
        const birthdate = language === 'es' ? 'Fecha de cumpleaños:' : 'Birthdate:';

        const kDrama = dramasArray[index];
        modalName.textContent = kDrama.name;
        modalBirthdate.textContent = `${birthdate} ${kDrama.birthDate}`;
        modalBiography.textContent = kDrama.biography;
        modalOtherWorks.innerHTML = kDrama.otherWorks.map(work => `<li>${work}</li>`).join('');
        
        // Limpiar imágenes previas
        modalImages.innerHTML = '';

        // Agregar imágenes a la galería
        kDrama.imagesActor.forEach(image => {
            const imgElement = document.createElement('img');
            imgElement.src = image;
            imgElement.alt = kDrama.name;
            imgElement.classList.add('modal-image');
            modalImages.appendChild(imgElement);
        });

        modal.style.display = "block";
    }

    dramas.forEach((drama, index) => {
        drama.addEventListener('click', () => {
            updateModalContent(index);
        });
    });

    closeModal.onclick = function() {
        modal.style.display = "none";
    }

    window.onclick = function(event) {
        if (event.target == modal) {
            modal.style.display = "none";
        }
    }
});
